import turtle
turtle.shape('turtle')
square = turtle.clone()
square.shape('square')

square.penup()
square.goto(100,100)
square.pendown()
square.goto(100+100,100)
square.stamp()
square.goto(100+100,100-100)
square.stamp()
square.goto(100,100-100)
square.stamp()
square.goto(100,100)
triangle = turtle.clone()
triangle.shape('triangle')
triangle.penup()
triangle.goto(-100,100)
triangle.pendown()
triangle.goto(-200,100-75)
triangle.stamp()
triangle.goto(-300,100)
triangle.stamp()
triangle.goto(-100,100)
triangle.stamp()


turtle.mainloop()
